# -*- coding: utf-8 -*-
"""
Colors class with supported colors for PJNturtle
"""


class Color:

    def __init__(self, r, g, b):
        self.r = r # red component
        self.g = g # green component
        self.b = b # blue component
        
    def get_rgb(self):
        return (self.r, self.g, self.b)
    
 

    
    



